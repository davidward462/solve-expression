import pytest
import solve

@pytest.mark.parametrize("expr, result", [
        ("1 + 1", 2),
        (" 4 / 2", 2),
        (" 5 / 2", 2.5),
        ("10 / 3", (10/3)),
        ("( 1024 / 8 ) + 1", 129),
        ("( 1024 / 7 ) + 9", (1024/7)+9),
        ("1000000000", 1000000000),
        ("32 + 6 * 2 + 6 - 1 + 285 * 45 - 1 * 99", 12775),
        ("( 100 - 2 * 22 ) * ( 9 - 10 ) + ( 100 / 4 ) * ( ( 3 - 252 ) + ( 4 * 5 ) )", -5781.0),
        ("1 / 0", None),
        ("", None),
        ("234 * 2 - 6 + 5 + ( 1 / 0 ) * 3 + 325", None),
        ("5 + 5", 10),
        ("( 1 / 2 ) + 1", 1.5)
        ])

def test_solve(expr, result):
        assert solve.solveExpression(expr) == result



